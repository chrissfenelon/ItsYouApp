// Helper function pour obtenir des questions aléatoires avec balance entre catégories et difficultés
export const getRandomQuestions = (count: number = 10): QuizQuestion[] => {
  // Distribution équilibrée des catégories pour 10 questions
  const categoryDistribution = {
    'compatibility': 2,
    'personality': 1,
    'scenarios': 2,
    'would_you_rather': 1,
    'fun': 1,
    'intimacy': 3, // Catégorie intimité/sexe
  };

  // Distribution des difficultés
  const difficultyDistribution = {
    'facile': Math.ceil(count * 0.4), // 40%
    'moyen': Math.ceil(count * 0.4),  // 40%
    'difficile': Math.floor(count * 0.2), // 20%
  };

  const selectedQuestions: QuizQuestion[] = [];

  // Sélectionner des questions par catégorie
  Object.entries(categoryDistribution).forEach(([categoryId, targetCount]) => {
    const categoryQuestions = QUIZ_QUESTIONS.filter(q => q.categoryId === categoryId);
    const shuffled = [...categoryQuestions].sort(() => 0.5 - Math.random());
    selectedQuestions.push(...shuffled.slice(0, targetCount));
  });

  // Si on n'a pas assez de questions, compléter avec des questions aléatoires
  if (selectedQuestions.length < count) {
    const remaining = QUIZ_QUESTIONS.filter(q => !selectedQuestions.includes(q));
    const shuffled = [...remaining].sort(() => 0.5 - Math.random());
    selectedQuestions.push(...shuffled.slice(0, count - selectedQuestions.length));
  }

  // Mélanger l'ordre final
  return selectedQuestions.sort(() => 0.5 - Math.random()).slice(0, count);
};

// Helper function pour obtenir des questions par catégorie
export const getQuestionsByCategory = (categoryId: string): QuizQuestion[] => {
  return QUIZ_QUESTIONS.filter(q => q.categoryId === categoryId);
};

// Helper function pour obtenir des questions par difficulté
export const getQuestionsByDifficulty = (difficulty: 'facile' | 'moyen' | 'difficile'): QuizQuestion[] => {
  return QUIZ_QUESTIONS.filter(q => q.difficulty === difficulty);
};
