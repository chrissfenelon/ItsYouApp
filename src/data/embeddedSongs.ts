export const loadEmbeddedSongs = (): Song[] => {
  // Convert embedded song URIs to Android resource format
  // For Android: android.resource://[package]/raw/[filename_without_extension]
  return embeddedSongs.map(song => ({
    ...song,
    uri: song.isEmbedded
      ? `android.resource://com.itsyouapp/raw/${song.uri.replace(/\.(mp3|m4a)$/, '')}`
      : song.uri
  }));
};
